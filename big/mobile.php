    <p class="para"> 
	This is where the importance of a mobile app shines. It creates a unique and dedicated platform for your users. It creates for them an experience that’s most optimal for those whose main source of internet comes in the form of their phone. This will prolong visits and build your brand. It will also force the site to reflect on what is most important since it has to consider the viewport screen and what would be most optimal for user experience.
    </p>
    
    <p class="para">
	In conclusion, responsive web is easier to create, faster to fix, and most likely the first experience your user, mobile or otherwise will view and judge your content on. Web Apps are most optimal for user experience for your visitors, this is where your company can tailor the experience to a vision that responsive web cannot. So what do you chose? The first question to ask is "do you need a Web App?” Will your users be willing to download a full fledged app to see your content, or can they receive the information just the same on their phone’s browser, and if necessary on their desktop for a more investigative visit. I tend to side on creating the best user experience for the mobile responsive web, It will create focus on the content of the page, and should convey any message you have to your visitor creating a want not a need for a more dedicated mobile application. A Mobile App should be complimentary to your site, built upon the trust that your responsive web has created, not to replace it.
